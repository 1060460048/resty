package cn.dreampie.common.util.json;

/**
 * DBException
 */
public class ObjectCastException extends RuntimeException {

  public ObjectCastException() {
  }

  public ObjectCastException(String message) {
    super(message);
  }

  public ObjectCastException(Throwable cause) {
    super(cause);
  }

  public ObjectCastException(String message, Throwable cause) {
    super(message, cause);
  }
}










